export function translate(key: string, lang: string, ...args: any[]): string {
  const trimmedKey = key.trim();
  let translated = trimmedKey;
  
  if (lang === "ta") {
    translated = tamilTranslations[trimmedKey] || trimmedKey;
    
    // Fallback search for matching pattern with %s
    if (translated === trimmedKey) {
      for (const [k, v] of Object.entries(tamilTranslations)) {
        if (k.includes("%s")) {
          const prefix = k.split("%s")[0];
          if (prefix && trimmedKey.startsWith(prefix)) {
            translated = v;
            break;
          }
        }
      }
    }
  }
  
  // Interpolation for %s
  if (translated.includes("%s") && args.length > 0) {
    let index = 0;
    return translated.replace(/%s/g, () => String(args[index++] ?? ""));
  }
  
  return translated;
}
